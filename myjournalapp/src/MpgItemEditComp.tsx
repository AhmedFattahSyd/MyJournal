///////////////////////////////////////////////////////////////////////////////////////////////
// MPG item Edit component edit
// allows the user to create or update item name, description, priority
///////////////////////////////////////////////////////////////////////////////////////////////
import * as React from "react";
import { Typography } from "@material-ui/core";
import MpgLogger from "./MpgLogger";
import MpgItem from "./MpgItem";
import MpgTheme from "./MpgTheme";
import MpgGraph, { MpgDisplayMode } from "./MpgGraph";
import { withRouter, RouteComponentProps } from "react-router-dom";
///////////////////////////////////////////////////////////////////////////////////////////////
// define interfaces for state and props
///////////////////////////////////////////////////////////////////////////////////////////////
interface IItemEditCompProps extends RouteComponentProps {
  toggleSidebarVisibility: (
    event: React.MouseEvent<HTMLSpanElement, MouseEvent>
  ) => void;
  mpgGraph: MpgGraph;
  mpgLogger: MpgLogger;
  item: MpgItem;
  displayMode: MpgDisplayMode
}
interface IItemEditCompState {
  item: MpgItem;
  displayMode: MpgDisplayMode
}
///////////////////////////////////////////////////////////////////////////////////////////////
// MPG Item edit component class
///////////////////////////////////////////////////////////////////////////////////////////////
class MpgItemEditCompBase extends React.Component<
  IItemEditCompProps,
  IItemEditCompState
> {
  constructor(props: IItemEditCompProps) {
    super(props);
    this.state = {
      item: props.item,
      displayMode: props.displayMode,
    };
  }
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // render item edit
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  render = () => {
    return <div>Item Edit</div>;
  };
  //////////////////////////////////////////////////////////////////////////////////////////////
  // component will receive props
  ///////////////////////////////////////////////////////////////////////////////////////////////
  componentWillReceiveProps(newProps: IItemEditCompProps) {
    this.setState({
      item: newProps.item,
      displayMode: newProps.displayMode,
    });
  }
}
///////////////////////////////////////////////////////////////////////////////////////////////
// wrap the component withRouter
///////////////////////////////////////////////////////////////////////////////////////////////
const MpgItemEditComp = withRouter(MpgItemEditCompBase);
export default MpgItemEditComp;
